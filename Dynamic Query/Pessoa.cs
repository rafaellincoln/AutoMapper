namespace Dynamic_Query
{
    public class Pessoa
    {
        private string nome;

        public string Nome
        {
            get { return nome; }
            set { nome = value; }
        }

        private string sobreNome;

        public string SobreNome
        {
            get { return sobreNome; }
            set { sobreNome = value; }
        }

        private int idade;

        public int Idade
        {
            get { return idade; }
            set { idade = value; }
        }

    }
}